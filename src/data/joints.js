// Helper function to get all movements as flat array
export const getAllMovements = () => {
    const allMovements = [];
    Object.values(JOINTS).forEach(category => {
        category.forEach(joint => {
            joint.movements.forEach(movement => {
                allMovements.push({
                    ...movement,
                    jointName: joint.name,
                    jointId: joint.id,
                    categoryId: Object.keys(JOINTS).find(key =>
                        JOINTS[key].some(j => j.id === joint.id)
                    ),
                });
            });
        });
    });
    return allMovements;
};

// Get movement by ID
export const getMovementById = (movementId) => {
    return getAllMovements().find(m => m.id === movementId);
};

